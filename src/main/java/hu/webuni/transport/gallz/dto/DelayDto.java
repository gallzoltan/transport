package hu.webuni.transport.gallz.dto;

public class DelayDto {
	
	private Long id;	
	private Integer delay;

	public DelayDto() {
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Integer getDelay() {
		return delay;
	}

	public void setDelay(Integer delay) {
		this.delay = delay;
	}

}
